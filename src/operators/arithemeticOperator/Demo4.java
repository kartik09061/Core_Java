package operators.arithemeticOperator;

public class Demo4 {
    static void main(String[] args) {
        // Shorthand Arithemetic Operator

        int a = 20;
        a += 10;
        System.out.println(a);

        int b = 20;
        b -= 10;
        System.out.println(b);

        int c = 20;
        c *= 10;
        System.out.println(c);

        int d = 20;
        d /= 10;
        System.out.println(d);

        int e = 200;
        e %= 20;
        System.out.println(e);
    }
}
