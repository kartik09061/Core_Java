package operators.relationalOperator;

public class Demo1 {
    static void main(String[] args) {
        System.out.println(10>3);
        System.out.println(10<3);
        System.out.println(10>=3);
        System.out.println(10<=3);
        System.out.println(10==3);
        System.out.println(10!=3);
    }
}
